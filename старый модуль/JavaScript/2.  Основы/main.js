/* Модуль программы считывание двух числе и сравнение их между собой */

alert("Модуль программы считывание двух числе и сравнение их между собой");

let errorsList = "";
let first = prompt("Введите первое число").replace(/\s/g, '');

if (!isFinite(first) || first === "") {
	errorsList += "\nПервый ввод содержит не число\n";
}

let second = prompt("Введите второе число").replace(/\s/g, '');

if (!isFinite(second)  || first === "") {
	errorsList += "Второй ввод содержит не число\nОбновите страницу и введите данные заново\n";
}

if (first == null || second == null)
	errorsList += "Поля ввода были пусты\nОбновите страницу и введите данные заново\n";

if (errorsList) {
	alert(errorsList);
} else {
	if (first > second) {
		alert("Первое число больше второго");
	} else if (first < second) {
		alert("Второе число больше первого");
	} else {
		alert("Числа равны");
	}	
}
