/* Модуль программы подсчета суммы введенных чисел */

alert("Модуль программы подсчета суммы введенных чисел");

let sum = 0;

let number = prompt("Введите число").replace(/\s/g, '');
while (number != null) {
	if (isFinite(number) && number !== "") {
		sum += parseInt(number);
	}
	number = prompt("Введите число");
}
alert(`Сумма введенных чисел равна: ${sum}`);
